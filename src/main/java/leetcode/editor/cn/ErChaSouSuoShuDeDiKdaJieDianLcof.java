    public class TreeNode {
        int val;
        TreeNode left;
        TreeNode right;

        TreeNode(int x) {
            val = x;
        }
    }

    //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {
        int ans, count;

        public int kthLargest(TreeNode root, int k) {
            count = k;
            dfs(root);
            return ans;
        }

        private void dfs(TreeNode node) {
            if (node != null) {
                dfs(node.right);
                if (count == 0) {
                    return;
                }
                if (--count == 0) {
                    ans = node.val;
                }
                dfs(node.left);
            }
        }
    }
